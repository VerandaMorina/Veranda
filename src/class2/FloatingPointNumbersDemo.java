package class2;

public class FloatingPointNumbersDemo {
    public static void main(String[] args) {
        /*
        data types to store floating point nr/decimal nr
        Whenever we have to store fn like 10.5, 2.3, -5.6, 0.00001 etc
        When we have this types of nr we use
        -float
        -double
        99% of the time double is used from us
        by default all the floating point nr are double we need to add F
         */

        float grocery=20.5F; //smaller size data type
        System.out.println(grocery);
        double price=1111.222222; //is the bigger in size can hold double the amount of float
        System.out.println(price);
    }
}
