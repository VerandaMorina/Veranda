package class3;

public class NameOfMonth {

    public static void main(String[] args) {

        int month=11;
        if (month==10)
        {
            System.out.println("October");
        }
        if (month%2==0)
        {
            System.out.println("Even");
        }
        else
        {
            System.out.println("Odd");
        }
    }
}
