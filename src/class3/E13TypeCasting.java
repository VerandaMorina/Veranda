public class E13TypeCasting {
    public static void main(String[] args) {
        //byte=short=int=long=float=double smallest to biggest
        byte num=127;
        short num2=num;
        System.out.println(num2);

        int num3=111122;
        double num4=num3;
        System.out.println(num3);
    }
}
